# Remove Duplicate Items in a List

given_list = [6, 1, 2, 3, 3, 4, 5, 5, 5]  # list(map(int, input().split()))
print(given_list)

print("----------------")

unique_items_set = {*given_list}
print(unique_items_set)

unique_items_list = list(unique_items_set)
print(unique_items_list)

print("====================")



